import assert from 'node:assert/strict';
import { readFile } from 'node:fs/promises';
import test from 'node:test';

const viewModelSource = await readFile(new URL('./runtime/view-model.js', import.meta.url), 'utf8');
const pageSource = await readFile(new URL('../app/admin/(console)/page.js', import.meta.url), 'utf8');

test('admin overview view model pulls the admin snapshot from the single cockpit surface', () => {
  const adminOverviewSection = viewModelSource.match(/export async function getAdminOverviewView\(\) \{[\s\S]*?\n\}/)?.[0] || '';
  assert.match(viewModelSource, /function readCockpitDashboard\(cockpit\)/);
  assert.match(viewModelSource, /function summarizeDeploymentOverview\(cockpit, daemonPayload\)/);
  assert.match(adminOverviewSection, /const timeoutMs = 2000/);
  assert.match(adminOverviewSection, /safeLayerOs\('\/api\/layer-os\/cockpit', \{ timeoutMs \}\)/);
  assert.doesNotMatch(adminOverviewSection, /fetchLayerOs\('\/api\/layer-os\/review-room'\)/);
  assert.doesNotMatch(adminOverviewSection, /fetchLayerOs\('\/api\/layer-os\/jobs\?limit=16'\)/);
  assert.doesNotMatch(adminOverviewSection, /fetchLayerOs\('\/api\/layer-os\/daemon'\)/);
  assert.match(adminOverviewSection, /const dashboard = readCockpitDashboard\(cockpit\)/);
  assert.match(adminOverviewSection, /const jobs = summarizeJobs\(asArray\(dashboard\.open_jobs\)\);/);
  assert.match(adminOverviewSection, /const review = summarizeReview\(dashboard\.review_room \|\| cockpit\.review_summary, null\);/);
  assert.match(adminOverviewSection, /const founderSummary = dashboard\.founder_summary \|\| cockpit\.founder_summary;/);
  assert.match(adminOverviewSection, /const founderView = dashboard\.founder_view \|\| cockpit\.founder_view;/);
  assert.match(adminOverviewSection, /const sourceIntake = summarizeSourceIntakeDashboard\(dashboard\.source_intake \|\| cockpit\.source_intake\);/);
  assert.match(adminOverviewSection, /const founderAttentionSnapshot = summarizeFounderAttentionSnapshot\(dashboard\.founder_attention\)/);
  assert.match(adminOverviewSection, /const founderAttentionFallback = summarizeFounderAttention\(founderSummary, founderView, review, jobs\)/);
  assert.match(adminOverviewSection, /const founderAttention = cockpitResult\.ok \? \(founderAttentionSnapshot \|\| null\) : \(founderAttentionSnapshot \|\| founderAttentionFallback\)/);
  assert.match(adminOverviewSection, /review,\s*\n\s*jobs,/);
  assert.match(adminOverviewSection, /founderAttention,/);
  assert.match(adminOverviewSection, /const primaryAttentionSnapshot = summarizeFounderAttentionSnapshot\(dashboard\.primary_attention\)/);
  assert.match(adminOverviewSection, /const primaryAttentionFallback = summarizeOverviewPrimaryAttention\(founderAttention \|\| founderAttentionFallback, jobs, sourceIntake\)/);
  assert.match(adminOverviewSection, /const primaryAttention = cockpitResult\.ok \? \(primaryAttentionSnapshot \|\| founderAttention \|\| null\) : \(primaryAttentionSnapshot \|\| primaryAttentionFallback\)/);
  assert.match(adminOverviewSection, /const founderRef = typeof founderSummary\?\.primary_ref === 'string' && founderSummary\.primary_ref\.startsWith\('flow_'\)/);
  assert.match(adminOverviewSection, /const founderFlowSnapshot = summarizeFounderFlowDefaultsSnapshot\(dashboard\.founder_flow_defaults\)/);
  assert.match(adminOverviewSection, /const founderFlowDefaults = cockpitResult\.ok \? \(founderFlowSnapshot \|\| \{\}\) : \(founderFlowSnapshot \|\| founderFlowFallback\)/);
  assert.match(adminOverviewSection, /primaryAttention,/);
  assert.match(adminOverviewSection, /founderFlowDefaults,/);
  assert.match(adminOverviewSection, /sourceIntake,/);
  assert.match(viewModelSource, /attentionHint:\s*attentionSource \?/);
  assert.match(viewModelSource, /function summarizeJobCounts\(snapshot\)/);
  assert.match(viewModelSource, /function summarizeFounderAttentionSnapshot\(snapshot\)/);
  assert.match(viewModelSource, /function summarizeFounderFlowDefaultsSnapshot\(snapshot\)/);
  assert.match(viewModelSource, /const jobCounts = summarizeJobCounts\(dashboard\.job_counts \|\| \{\}\);/);
  assert.match(viewModelSource, /function summarizeFounderAttention\(founderSummary, founderView, review, jobs\)/);
  assert.match(viewModelSource, /function summarizeSourceIntakeDashboard\(snapshot\)/);
  assert.match(viewModelSource, /function summarizeOverviewPrimaryAttention\(founderAttention, jobs, sourceIntake\)/);
  assert.match(viewModelSource, /function summarizeFounderFlowDefaults\(founderSummary, founderAttention, primaryAttention, founderRef, jobs, review\)/);
  assert.match(viewModelSource, /actionCount:\s*Number\(snapshot\?\.action_count/);
  assert.match(viewModelSource, /quietCount:\s*Number\(snapshot\?\.quiet_count/);
  assert.match(viewModelSource, /prepCount:\s*Number\(snapshot\?\.prep_count/);
  assert.match(viewModelSource, /reviewCount:\s*Number\(snapshot\?\.review_count/);
  assert.match(viewModelSource, /prepReadyCount:\s*Number\(snapshot\?\.prep_ready_count/);
  assert.match(viewModelSource, /summaryNote:\s*asString\(snapshot\?\.summary_note\s*\?\?\s*snapshot\?\.summaryNote\)/);
  assert.match(viewModelSource, /summaryMeta:\s*asString\(snapshot\?\.summary_meta\s*\?\?\s*snapshot\?\.summaryMeta\)/);
  assert.match(viewModelSource, /quietNote:\s*asString\(snapshot\?\.quiet_note\s*\?\?\s*snapshot\?\.quietNote\)/);
  assert.match(viewModelSource, /case 'review_room':/);
  assert.match(viewModelSource, /case 'rollback_or_fix':/);
  assert.match(viewModelSource, /case 'approve':/);
  assert.match(viewModelSource, /vmTargetReady:\s*targets\.some\(\(item\)\s*=>\s*item\.target_id === 'vm'\)/);
  assert.match(viewModelSource, /continuityHost:/);
  assert.match(viewModelSource, /releaseStamp:/);
  assert.match(viewModelSource, /remoteReleaseDir:/);
  assert.match(viewModelSource, /latestRelease:/);
  assert.match(viewModelSource, /latestDeploy:/);
  assert.match(viewModelSource, /latestRollback:/);
  assert.match(adminOverviewSection, /jobCounts,/);
  assert.match(adminOverviewSection, /reviewNote:\s*asString\(dashboard\.review_note\)/);
  assert.match(adminOverviewSection, /reviewMeta:\s*asString\(dashboard\.review_meta\)/);
});

